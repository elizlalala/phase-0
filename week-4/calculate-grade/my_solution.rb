def get_grade(num_grade)
   
   # if it's greater than 90, the program will return an A
   if num_grade >= 90
       p "A"
   elsif num_grade >= 80
       p "B"
   elsif num_grade >= 70
       p "C"
   elsif num_grade >= 60
       p "D"
   else 
       p "F"
   end
   
   
end